namespace Plisky.CodeCraft {

    /// <summary>
    /// Allows the version number to display in different formats, short version format, full version format or with no display.
    /// </summary>
    public enum DisplayType {
        Default = 0x0000,
        Short = 0x0001,
        Full = 0x0002,
        NoDisplay = 0x0003,
        ThreeDigit = 0x0004
    }
}